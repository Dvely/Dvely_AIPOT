function applyVars(template: string, vars?: Vars): string {
  if (!vars) return template;
  return Object.entries(vars).reduce((acc, [key, value]) => {
    return acc.replace(new RegExp(`\\\\{${key}\\\\}`, "g"), String(value));
  }, template);
}

export function translate(language: PreferredLanguage, key: string, vars?: Vars): string {
  if (language === "en") {
    return applyVars(key, vars);
  }

  const dict = language === "ko" ? KO : JA;
  const translated = dict[key] ?? key;
  return applyVars(translated, vars);
}

export function useI18n() {
  const [language, setLanguage] = useState<PreferredLanguage>(getCurrentPreferredLanguage());

  useEffect(() => {
    const sync = () => {
      setLanguage(getCurrentPreferredLanguage());
    };

    window.addEventListener(SESSION_UPDATED_EVENT, sync as EventListener);
    window.addEventListener("storage", sync);

    return () => {
      window.removeEventListener(SESSION_UPDATED_EVENT, sync as EventListener);
      window.removeEventListener("storage", sync);
    };
  }, []);

  const t = useMemo(() => {
    return (key: string, vars?: Vars) => translate(language, key, vars);
  }, [language]);

  return { language, t };
}
